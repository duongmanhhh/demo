    private void loadData() {
        list = serGV.getAll(list);
    }
    
    private void showDataTable(List<GiangVien> list) {
        tblModel.setRowCount(0);
        for (GiangVien giangVien : list) {
            tblModel.addRow(giangVien.toDataRow());
        }
    }
    
    private void loadCboBac() {
        listBac.add(1);
        listBac.add(2);
        listBac.add(3);
    }
    
    private void loadCboLoai() {
        listLoai.add(1);
        listLoai.add(2);
    }
    
    private void showCboBac() {
        loadCboBac();
        for (Integer integer : listBac) {
            cboModel.addElement(integer);
        }
    }
    
    private void showCboLoai() {
        loadCboLoai();
        for (Integer integer : listLoai) {
            cboModel2.addElement(integer);
        }
    }
    
    private void findData(int index) {
        GiangVien gv = list.get(index);
        txtMa.setText(String.valueOf(gv.getMaNV()));
        txtTen.setText(gv.getTen());
        txtTuoi.setText(String.valueOf(gv.getTuoi()));
        cboBac.setSelectedItem(gv.getBac());
        if(gv.isGioiTinh()==true){
            rdoDuc.setSelected(true);
        }else{
            rdoCai.setSelected(true);
        }
        cboLoaiGV.setSelectedItem(gv.getLoaiGV().getId());
        
    }

