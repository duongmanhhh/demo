    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        int ma = Integer.parseInt(txtMa.getText());
        String ten = txtTen.getText();
        int tuoi = Integer.parseInt(txtTuoi.getText());
        int bac = (int) cboBac.getSelectedItem();
        boolean gt=rdoDuc.isSelected();
        String gender="";
        if(gt==true){
            gender="nam";
        }else{
            gender="nu";
        }
        LoaiGV loai = serLoai.getOne(Integer.parseInt(cboLoaiGV.getSelectedItem().toString()));
        
        GiangVien gv = new GiangVien(ma, ten, tuoi, bac, gt, loai);
        JOptionPane.showMessageDialog(this, serGV.add(gv));
        loadData();
        showDataTable(list);
    }//GEN-LAST:event_btnAddActionPerformed

